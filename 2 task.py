# Алгоритм решения:
#
# Подсчитываем, сколько льда мы можем добавить в каждую скульптуру, чтобы сделать её идеальной.
# Подсчитываем, сколько льда мы можем удалить из каждой скульптуры, чтобы сделать её идеальной.
# Подсчитываем, сколько скульптур мы можем сделать идеальными, если будем только добавлять лед в каждую из них.
# Подсчитываем, сколько скульптур мы можем сделать идеальными, если будем только удалять лед из каждой из них. Для этого проходимся по всем скульптурам и проверяем, можем ли мы удалить достаточно льда, чтобы сделать её идеальной, и успеем ли мы это сделать до наступления праздника.
# Выводим максимально возможное количество идеальных скульптур и их номера.

n, m, k = map(int, input().split())
weights = list(map(int, input().split()))

num_perfect = 0
time_left = k

for w in weights:
    time_needed = abs(w - m)

    if time_needed <= time_left:
        num_perfect += 1
        time_left -= time_needed

if num_perfect == 0:
    print(0)
else:
    print(num_perfect)
    print(" ".join(str(i + 1) for i, w in enumerate(weights) if abs(w - m) <= k))
